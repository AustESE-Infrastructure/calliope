    {
        // compose an appropriate URL and send it
        try
        {
            String rawUrl = "http://localhost:8080/html/comparison/english/"
                +"shakespeare/kinglear/act1/scene1/F1";
            rawUrl = URLEncoder.addGetParam(rawUrl,Params.SHORTNAME,"F2");
            URL url = new URL( rawUrl );
            URLConnection conn = url.openConnection();
            InputStream is = conn.getInputStream();
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            while ( is.available() != 0 )
            {
                byte[] data = new byte[is.available()];
                is.read( data );
                bos.write( data );
                // do more here like check the syntax of the output
            }
            Multipart mp = Multipart.internalise( bos.toString() );
            // first check that each part is text/html
            boolean success = true;
            if ( mp.numParts() != 2 )
            {
                System.out.println("should be two mime parts but found "
                    +mp.numParts());
                success = false;
            }
            // now parse the HTML and check it's syntacticaly correct 
            // and that each side contains the correct tags
            if ( success )
            {
                Part p1 = mp.getPart( 0 );
                Part p2 = mp.getPart( 1 );
                Header h1 = p1.getHeader();
                Header h2 = p2.getHeader();
                if ( !h1.getContentType().equals(MIMETypes.HTML) )
                {
                    System.out.println("expected HTML type LHS");
                    success = false;
                }
                if ( !h2.getContentType().equals(MIMETypes.HTML) )
                {
                    System.out.println("expected HTML type RHS");
                    success = false;
                }
                if ( h1.getName()==null || !h1.getName().equals(MMP.LHS) )
                {
                    System.out.println("incorrect name for LHS");
                    success = false;
                }
                if ( h2.getName()==null || !h2.getName().equals(MMP.RHS) )
                {
                    System.out.println("incorrect name for RHS");
                    success = false;
                }
                // also check that there are tags called "deleted" and 
                // "added" in the appropriate sides
                try
                {
                    Parser p = Parser.createParser( p1.getBody(), 
                        h1.getEncoding() );
                    System.out.println(p2.getBody());
                    NodeList nl = p.parse( new MyNodeFilter("SPAN",
                        ChunkState.DELETED) );
                    if ( nl.size() == 0 )
                    {
                        System.out.println("no deleted spans found in LHS");
                        success = false;
                    }
                }
                catch ( ParserException pe )
                {
                    System.out.println( pe.getMessage() );
                    success = false;
                }
                try
                {
                    Parser p = Parser.createParser( p2.getBody(), 
                        h2.getEncoding() );
                    NodeList nl = p.parse( new MyNodeFilter("SPAN",
                        ChunkState.ADDED) );
                    if ( nl.size() == 0 )
                    {
                        System.out.println("no added spans found in RHS");
                        success = false;
                    }
                }
                catch ( ParserException pe )
                {
                    System.out.println( pe.getMessage() );
                    success = false;
                }
            }
            if ( success )
            {
                System.out.println( "Passed HTML compare test" );
                return 1;
            }
            else
            {
                System.out.println("Failed HTML compare test");
                return 0;
            }
        }
        catch ( Exception e )
        {
            System.out.println( "Failed HTML compare test: "
                +e.getMessage() );
            return 0;
        }
    }
    class MyNodeFilter implements NodeFilter
    {
        String propName;
        String tagName;
        MyNodeFilter( String tagName, String propName )
        {
            this.propName = propName;
            this.tagName = tagName;
        }
        public boolean accept( Node node ) 
        {
            if ( node instanceof Tag )
            {
                Tag t = (Tag) node;
                boolean result = t.getTagName().equals(tagName) 
                    && t.getAttribute("class")!= null 
                    && t.getAttribute("class").equals(propName);
                return result;
            }
            else
                return false;
        }
    }
